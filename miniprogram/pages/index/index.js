    const student = (await students
      .where({
        _openid: '{openid}'
      })
      .get()).data[0]
    const pages = student ? 'rank' : 'register'
    wx.setStorageSync('student', student)
    wx.redirectTo({
      url: `/pages/${pages}/${pages}`,
    })
